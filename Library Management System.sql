---------------------------------------------------------------------------------------------------------------------------------------------

create table Section				(S_id varchar(15) primary key not null,
									 S_Name varchar(100) not null)

create table Publisher				(P_id varchar(5) primary key not null,
									 P_Name varchar(200) not null,
									 Country varchar(50) not null,
									 cellNo varchar(15))

create table Author					(A_id varchar(5) primary key not null,
									 A_Name varchar(50) not null,
									 CNIC varchar(50) not null,
									 Country varchar(50) not null,
									 cellNo varchar(15))

create table Member					(M_Id int primary key not null,
									 M_Name varchar(50) not null,
									 CNIC varchar(50) not null,
									 Street varchar(50) not null,
									 cellNo varchar(15) not null)

create table Newspaper				(Name varchar(50) primary key not null,
									 Publisher varchar(5) foreign key references Publisher(P_Id) not null,
									 Section varchar(15) not null foreign key references Section(S_Id)) 

create table NewspapersInLib		(Name varchar(50) foreign key references Newspaper(Name),
									 Date date not null,)

create table Magazine				(MagName varchar(50) primary key not null,
									 Publisher varchar(5) foreign key references Publisher(P_Id) not null,
									 Section varchar(15) foreign key references Section(S_Id) not null)

create table MagazineInLib			(MagName varchar(50) not  null foreign key references Magazine(MagName),
									 MagMonth varchar(15) not null,
									 MagYear varchar(5) not null)


create table Librarian				(L_Id varchar(2) not null primary key,
									 Name varchar(50) not null,
									 cellNo varchar(15) not null,
									 CNIC varchar(50) not null,
									 address varchar(100) not null,
									 shiftStartTime varchar(15) not null,
									 shiftEndTime varchar(15) not null)

create table lib_data				(libName varchar(50) not null primary key,
									 totalMembers int not null,
									 totalBooks int not null,
									 totalMags int not null,
									 totalNewsPapers int not null)

create table Book					(ISBN varchar(50) primary key not null,
									 B_Title varchar(50) not null,
									 P_Id varchar(5) not null foreign key references Publisher(P_Id),
									 S_Id varchar(15) not null foreign key references Section(S_Id),
									 A_Id varchar(5) not null foreign key references Author(A_Id))

create table BooksInLib				(BookId int primary key not null,
									 ISBN varchar(50) not null foreign key references Book(ISBN),
									 Language varchar(50) not null,
									 Edition int not null)

create table Borrow					(M_Id int not null foreign key references Member(M_Id),
									 BookId int not null foreign key references BooksInLib(BookId),
									 dueDate varchar(15) not null,
									 lateFine int,
									 Librarian varchar(2) not null foreign key references Librarian(L_Id))

create table Record					(R_Id int primary key not null,
									 MemberId int foreign key references Member(M_Id) not null,
									 BookId int foreign key references BooksInLib(BookId) not null,
									 BorrowDate varchar(15) not null)

